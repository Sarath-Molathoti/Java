public class Alphabets{
	public static void main(String args[]){
		char ch = 'A';
		while(ch <= 'Z'){
		     System.out.print(ch + " ");
		     ch++;
		}
		System.out.println();
	       ch = 'a';
	       while(ch <= 'z'){
		     System.out.print(ch + " ");
		     ch++;
		}
		System.out.println();
	}
}
